public class MediaEsp {

    public static void main(String[] args) {
        
        double mediaa, mediab, mediac;
        
        mediaa = (7+8+9)/3;
        mediab = (4+5+6)/3;
        mediac = (mediaa+mediab)/2;
        
        System.out.println("Aqui está a média dos números 7, 8 e 9: " + mediaa);
        System.out.println("\nAqui está a média dos números 4, 5 e 6: " + mediab);
        System.out.println("\nAqui está a média das duas médias anteriores: " + mediac);
    }
    
}
